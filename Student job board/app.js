function cvCollectionHasContent(list){
  return Array.isArray(list) && list.some(item =>
    Object.values(item || {}).some(value => hasText(value))
  );
}
function hasSavedCVBuilder(){
  const cv = getSavedCVBuilder();
  if(!cv) return false;

  const basicFields = [
    cv.fullName, cv.role, cv.email, cv.phone, cv.location,
    cv.website, cv.summary, cv.skills, cv.languages
  ];

  return basicFields.some(hasText)
    || cvCollectionHasContent(cv.education)
    || cvCollectionHasContent(cv.experience)
    || cvCollectionHasContent(cv.projects);
}
