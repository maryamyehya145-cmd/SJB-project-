function bindOAuthButtons(){
  const form = document.getElementById("authForm");
  const buttons = document.querySelectorAll(".oauthBtn");
  if(!form || !buttons.length) return;

  buttons.forEach(button => {
    button.addEventListener("click", ()=>{
      const provider = button.dataset.provider || "social";
      const timestamp = Date.now();

      const emailInput = document.getElementById("authEmail");
      const passwordInput = document.getElementById("authPassword");
      const nameInput = document.getElementById("authName");

      if(emailInput) emailInput.value = `${provider}${timestamp}@sjb.demo`;
      if(passwordInput) passwordInput.value = "123456";
      if(nameInput && !nameInput.value.trim()) nameInput.value = `${provider.toUpperCase()} User`;

      if(form.requestSubmit) form.requestSubmit();
      else form.dispatchEvent(new Event("submit", { bubbles: true, cancelable: true }));
    });
  });
}

